# have similar operations like strings
names = ['John', 'Bob', 'Mosh', 'Sarah', 'Mary']
print(names)

# accessing item by index
print(names[0])
print(names[-1])  # for reverse traversal

# print from a range
print(names[2:])  # from index 2 to the last
print(names[2:4])

# modifying items from the list
names[0] = "John Edited"
print(names)
